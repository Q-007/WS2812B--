# ========== 配置说明 ==========
# 
# IP地址说明：
#   - 必须是合法的私有IP地址
#   - 格式：xxx.xxx.xxx.xxx（每个xxx是0-255之间的数字）
#   - 示例：192.168.52.13, 192.168.1.100, 192.168.13.14
#   - 注意：不能使用 192.168.521.1314（每段超过255）
# 
# WiFi密码说明：
#   - None：开放网络，无需密码（方便但不安全）
#   - "12345678"：设置密码（至少8个字符，更安全）
# 
# GPIO引脚说明：
#   - 可用引脚：0, 2, 4, 5, 12-19, 21-23, 25-27, 32-33
#   - 避免使用：1, 3 (串口), 6-11 (Flash)
#   - 推荐使用：4, 5, 12-15, 18, 19, 21-23
# 
# 图案保存说明：
#   - 图案保存在Flash中，重启不丢失
#   - 每个图案约占用200-300字节
#   - 默认Flash可保存数百个图案
#   - 定期备份 patterns.json 文件
# 
